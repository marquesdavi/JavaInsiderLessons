package javacore;

public class VarType {
    public static void main(String[] args){
        var tst = 10; // Definição de variável de forma dinâmica
        System.out.println(tst);
        var lonG = 2209409304934L; // Suporta todos os tipos do Java. A definição é feita pelo compilador
        System.out.println(lonG);

        // NÃO significa que a variável pode ter tipo de dado reatribuído
    }
}
