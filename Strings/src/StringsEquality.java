public class StringsEquality {
    public static void main(String[] args){
        String a = "comida";
        String b = "a";

        // O metodo ".equals()" retorna true ou false na comparação de uma string com outra
        if (a.equals(b)) System.out.println("Está igual");
        else {
            System.out.println("Está diferente");
        }
    }
}
