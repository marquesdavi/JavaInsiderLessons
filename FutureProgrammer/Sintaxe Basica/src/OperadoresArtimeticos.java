public class OperadoresArtimeticos {
    public static void main(String[] args){
        int a = 10;
        int b = 6;

        int soma = a + b; // Soma com o operador "+"
        System.out.println("A soma de A e B é: " + soma);


        int subtracao = a - b; // Subtração com o operador "-"
        System.out.println("A subtração de A e B é: " + subtracao);

        int multiplicacao = a * b; // Multiplicação com o operador "*"
        System.out.println("A multiplicado por B é: " + multiplicacao);

        double divisao = a / b; // Divisão com o operador "/"
        System.out.println("A dividido por B é: " + divisao);

        int restoDivisao = a % b; // Resto da divisão com o operador "%"
        System.out.println("O resto da divisão de A por B é " + restoDivisao);

    }
    
}
