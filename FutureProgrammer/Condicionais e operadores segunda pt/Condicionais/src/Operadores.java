public class Operadores {
    public static void main(String[] args){
        System.out.println("Operadores Relacionais");

        int a = 12;
        int b = 20;

        System.out.println("A é igual a B? " + (a == b)); // Retorna false
        System.out.println("A é menor ou igual a B? " + (a <= b)); // Retorna true
        System.out.println("A é diferente de B? " + (a != b)); // Retorna true
        System.out.println("A é maior ou igual a B? " + (a >= b)); // Retorna false 
    }
}
